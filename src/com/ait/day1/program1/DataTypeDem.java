package com.ait.day1.program1;

public class DataTypeDem {

	public static void main(String[] args) {
		int v1=9/2;
		float v2=101/61f;
		double v3=10d/6d;
		float v4=5/2.5f;
		
		System.out.println("Value1 is:"+v1);
		System.out.println("Value2 is:"+v2);
		System.out.println("Value3 is:"+v3);
		System.out.println("Value4 is:"+v4);

	}

}
